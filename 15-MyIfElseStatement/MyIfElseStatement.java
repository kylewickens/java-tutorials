public class MyIfElseStatement {

public static void main(String args[]) {
	int i = 45;

	if (i < 30) {
		System.out.println("if statement has been triggered");
	} else if (i < 40) {
		System.out.println("else if statement has been triggered");
	} else {
		System.out.println("else statement has been triggered");
	}
}
}
